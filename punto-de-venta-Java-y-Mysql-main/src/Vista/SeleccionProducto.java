    public void filtrarProductosPorId(String criterio) {
        List<Productos> ListarProductos = proDao.ListarProductos();
        modelo = (DefaultTableModel) TableProducto.getModel();
        modelo.setRowCount(0);

        for (Productos producto : ListarProductos) {
            if (String.valueOf(producto.getId()).contains(criterio)) {
                Object[] ob = new Object[6];
                ob[0] = producto.getId();
                ob[1] = producto.getCodigo();
                ob[2] = producto.getNombre();
                ob[3] = producto.getProveedor();
                ob[4] = producto.getStock();
                ob[5] = producto.getPrecio();
                modelo.addRow(ob);
            }
        }

        TableProducto.setModel(modelo);
    }

    public void filtrarProductosPorNombre(String criterio) {
        List<Productos> ListarProductos = proDao.ListarProductos();
        modelo = (DefaultTableModel) TableProducto.getModel();
        modelo.setRowCount(0);
        for (Productos producto : ListarProductos) {
            if (producto.getNombre().toLowerCase().contains(criterio.toLowerCase())) {
                Object[] ob = new Object[6];
                ob[0] = producto.getId();
                ob[1] = producto.getCodigo();
                ob[2] = producto.getNombre();
                ob[3] = producto.getProveedor();
                ob[4] = producto.getStock();
                ob[5] = producto.getPrecio();
                modelo.addRow(ob);
            }
        }

        TableProducto.setModel(modelo);
    }

